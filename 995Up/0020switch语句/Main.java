public class Main{
    public static void main(String[] args){
        int status = 2;
        switch(status){
            case 0:
                System.out.println("Status is 0");
                break;
            case 1:
                System.out.println("Status is 1");
                break;
            case 2:
                System.out.println("Status is 2");
                break;
            case 3:
                System.out.println("Status is 3");
                break;
            default:
                System.out.println("Status is not listed");
        }
    }
}
